int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((int)str[i] >= 32 && (int)str[i] <= 126))
			return (0);
		i++;
	}
	return (1);
}
