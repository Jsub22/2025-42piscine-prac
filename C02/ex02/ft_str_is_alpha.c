int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((int)str[i] >= 97 && (int)str[i] <= 122) \
		&& !((int)str[i] >= 65 && (int)str[i] <= 90))
			return (0);
		i++;
	}
	return (1);
}
